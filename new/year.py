
month = int(input("The number of the month is : "))
if month >= 1 and month < 4:
    print("Winter 🌨️")
elif month >= 4 and month < 7:
    print("Spring 🌱")
elif month >= 7 and month < 10:
    print("Summer 🌻")
elif month >= 10 and month <= 12:
    print("Autumn 🍂")
else :
    print("Invalid")





